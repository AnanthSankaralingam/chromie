/**
 * esbuild Build Service
 *
 * Bundles Chrome extension files using esbuild.
 * - Server-side (Node.js): uses native esbuild (excluded via serverExternalPackages)
 * - Resolves relative imports via a virtual filesystem plugin
 * - Resolves bare module imports (npm packages) from pre-bundled vendor cache
 * - Non-JS/CSS files pass through unchanged
 */

import * as esbuild from 'esbuild'
import { readFileSync } from 'fs'
import { join, dirname } from 'path'
import { resolveProjectPackages } from './package-extractor.js'
import { getAllWhitelistedNames } from './package-whitelist.js'
import { augmentLodashVendorModule } from './lodash-vendor-augment.js'
import { augmentWebextensionPolyfillVendorModule } from './webextension-polyfill-vendor-augment.js'

// ── Vendor cache loader ───────────────────────────────────────────────────────

/** @type {Map<string, string> | null} */
let vendorCache = null

/**
 * Lazily load pre-bundled vendor packages into memory.
 * Returns a Map<packageName, bundledSourceCode>.
 */
function getVendorCache() {
  if (vendorCache) return vendorCache
  vendorCache = new Map()

  try {
    // Dynamic import won't work at build time — use require-style approach
    // The vendor directory is generated by scripts/prebundle-packages.js
    const vendorDir = join(process.cwd(), 'src', 'lib', 'build', 'vendor')
    const indexPath = join(vendorDir, 'index.js')

    // Read the generated index to get the manifest
    const indexContent = readFileSync(indexPath, 'utf-8')
    // Parse the manifest entries from the generated file
    const entryRegex = /\['([^']+)',\s*\{\s*version:\s*'([^']+)',\s*file:\s*'([^']+)'\s*\}\]/g
    let match
    while ((match = entryRegex.exec(indexContent)) !== null) {
      const [, pkgName, , fileName] = match
      try {
        const content = readFileSync(join(vendorDir, fileName), 'utf-8')
        vendorCache.set(pkgName, content)
      } catch {
        // Package file missing — skip silently
      }
    }
  } catch {
    console.warn('⚠️ [esbuild-service] Vendor cache not found. Run `npm run prebundle` first.')
  }

  return vendorCache
}

// ── esbuild plugins ──────────────────────────────────────────────────────────

/**
 * Virtual filesystem plugin — resolves imports against in-memory file map
 * @param {Record<string, string>} files - Map of file path → content
 */
function virtualFsPlugin(files) {
  return {
    name: 'virtual-fs',
    setup(build) {
      // Resolve relative imports within the virtual filesystem
      build.onResolve({ filter: /^\./ }, (args) => {
        // Resolve relative to the importer's directory
        const importerDir = args.importer ? dirname(args.importer) : '/'
        let resolved = join(importerDir, args.path).replace(/\\/g, '/')

        // Normalize leading slash
        if (!resolved.startsWith('/')) resolved = '/' + resolved

        // Try exact match, then with .js extension
        if (files[resolved]) {
          return { path: resolved, namespace: 'virtual' }
        }
        if (files[resolved + '.js']) {
          return { path: resolved + '.js', namespace: 'virtual' }
        }
        // Try index.js
        if (files[resolved + '/index.js']) {
          return { path: resolved + '/index.js', namespace: 'virtual' }
        }

        return { path: resolved, namespace: 'virtual' }
      })

      // Load files from the virtual filesystem
      build.onLoad({ filter: /.*/, namespace: 'virtual' }, (args) => {
        const content = files[args.path]
        if (content !== undefined) {
          const loader = args.path.endsWith('.css') ? 'css' : 'js'
          return { contents: content, loader }
        }
        return { errors: [{ text: `File not found in virtual FS: ${args.path}` }] }
      })
    }
  }
}

/**
 * Package resolver plugin — resolves bare module imports from vendor cache
 * @param {Set<string>} validPackages - Set of whitelisted package names that exist in vendor cache
 */
function packageResolverPlugin(validPackages) {
  return {
    name: 'package-resolver',
    setup(build) {
      // Match bare module specifiers (not starting with . or /)
      build.onResolve({ filter: /^[^./]/ }, (args) => {
        // Extract root package name
        const rootPkg = args.path.startsWith('@')
          ? args.path.split('/').slice(0, 2).join('/')
          : args.path.split('/')[0]

        if (validPackages.has(rootPkg)) {
          return { path: rootPkg, namespace: 'vendor-pkg' }
        }

        // Not a whitelisted package — fail the build (Chrome extensions cannot load npm packages at runtime)
        return {
          errors: [{
            text: `Package "${rootPkg}" is not whitelisted. Only whitelisted packages can be used in extensions. Remove this import or use a whitelisted alternative.`
          }]
        }
      })

      // Load pre-bundled package content from vendor cache
      build.onLoad({ filter: /.*/, namespace: 'vendor-pkg' }, (args) => {
        const cache = getVendorCache()
        const content = cache.get(args.path)
        if (content) {
          let out = content
          if (args.path === 'lodash') out = augmentLodashVendorModule(content)
          else if (args.path === 'webextension-polyfill') {
            out = augmentWebextensionPolyfillVendorModule(content)
          }
          return { contents: out, loader: 'js' }
        }
        return { errors: [{ text: `Vendor package not found: ${args.path}. Run \`npm run prebundle\`.` }] }
      })
    }
  }
}

// ── Main build function ───────────────────────────────────────────────────────

/**
 * @typedef {Object} BuildResult
 * @property {boolean} success
 * @property {Record<string, string>} files - All output files (bundled JS/CSS + passthrough)
 * @property {string[]} resolvedPackages - Packages that were resolved from vendor cache
 * @property {string[]} rejectedPackages - Packages not on whitelist
 * @property {Array<{file: string, message: string}>} errors
 * @property {Array<{file?: string, message: string}>} warnings
 * @property {boolean} fallback - True if returning unbundled originals
 */

/**
 * Bundle a Chrome extension's files using esbuild
 *
 * @param {Object} options
 * @param {Record<string, string>} options.files - Map of file_path → content
 * @param {Array<{name: string}>} [options.planPackages] - Packages from planning phase
 * @param {boolean} [options.minify=false] - Whether to minify output
 * @returns {Promise<BuildResult>}
 */
export async function buildExtension({ files, planPackages, minify = false }) {
  const errors = []
  const warnings = []
  console.log('[esbuild] buildExtension started')

  // Step 1: Extract and validate npm imports
  console.log('[esbuild] Step 1: resolving project packages...')
  const { valid, rejected } = resolveProjectPackages(planPackages || [], files)
  console.log(`[esbuild] Step 1 done: valid=${valid.map(v=>v.name)}, rejected=${rejected}`)

  if (rejected.length > 0) {
    const whitelistPreview = getAllWhitelistedNames().slice(0, 10).join(', ')
    errors.push({
      file: '',
      message: `Non-whitelisted packages are not supported: ${rejected.join(', ')}. Only whitelisted packages can be used (e.g. ${whitelistPreview}...). Remove these imports or use a whitelisted alternative.`
    })
    return {
      success: false,
      files: {},
      resolvedPackages: [],
      rejectedPackages: rejected,
      errors,
      warnings,
      fallback: true
    }
  }

  const validPackageNames = new Set(valid.map(v => v.name))

  // Check which valid packages are actually in the vendor cache
  console.log('[esbuild] Step 2: loading vendor cache...')
  const cache = getVendorCache()
  console.log(`[esbuild] Step 2 done: vendor cache has ${cache.size} packages`)
  const resolvedPackages = valid.filter(v => cache.has(v.name)).map(v => v.name)
  const missingFromCache = valid.filter(v => !cache.has(v.name)).map(v => v.name)
  if (missingFromCache.length > 0) {
    errors.push({
      file: '',
      message: `Whitelisted packages not in vendor cache: ${missingFromCache.join(', ')}.`
    })
    return {
      success: false,
      files: {},
      resolvedPackages: [],
      rejectedPackages: rejected,
      errors,
      warnings,
      fallback: true
    }
  }

  // Step 2: Normalize file paths (ensure leading /)
  const normalizedFiles = {}
  for (const [path, content] of Object.entries(files)) {
    const normalized = path.startsWith('/') ? path : '/' + path
    normalizedFiles[normalized] = content
  }

  // Step 3: Separate files by type
  const jsFiles = {}
  const cssFiles = {}
  const passthroughFiles = {}

  for (const [path, content] of Object.entries(normalizedFiles)) {
    if (path.endsWith('.js') || path.endsWith('.mjs')) {
      jsFiles[path] = content
    } else if (path.endsWith('.css')) {
      cssFiles[path] = content
    } else {
      passthroughFiles[path] = content
    }
  }

  const outputFiles = { ...passthroughFiles }

  // Step 4: Bundle each JS entry point
  console.log(`[esbuild] Step 4: bundling ${Object.keys(jsFiles).length} JS files: ${Object.keys(jsFiles).join(', ')}`)
  for (const [entryPath, content] of Object.entries(jsFiles)) {
    console.log(`[esbuild]   bundling ${entryPath} (${content.length} chars)...`)
    const fileStart = Date.now()
    try {
      const result = await esbuild.build({
        stdin: {
          contents: content,
          resolveDir: dirname(entryPath),
          sourcefile: entryPath,
          loader: 'js',
        },
        bundle: true,
        write: false,
        minify,
        format: 'iife',
        target: ['chrome120'],
        plugins: [
          virtualFsPlugin(normalizedFiles),
          packageResolverPlugin(validPackageNames),
        ],
      })

      console.log(`[esbuild]   bundled ${entryPath} in ${Date.now() - fileStart}ms`)
      if (result.errors.length > 0) {
        for (const err of result.errors) {
          errors.push({ file: entryPath, message: err.text })
        }
      }
      if (result.warnings.length > 0) {
        for (const warn of result.warnings) {
          warnings.push({ file: entryPath, message: warn.text })
        }
      }

      // Use bundled output
      const outputPath = entryPath.replace(/^\//, '')
      if (result.outputFiles && result.outputFiles.length > 0) {
        outputFiles[outputPath] = result.outputFiles[0].text
      } else {
        // Fallback to original if no output produced
        outputFiles[outputPath] = content
      }
    } catch (err) {
      console.error(`[esbuild]   ❌ error bundling ${entryPath} after ${Date.now() - fileStart}ms: ${err.message}`)
      errors.push({ file: entryPath, message: err.message })
      // On failure, include original unbundled file
      outputFiles[entryPath.replace(/^\//, '')] = content
    }
  }

  // Step 5: Transform CSS files
  console.log(`[esbuild] Step 5: transforming ${Object.keys(cssFiles).length} CSS files`)
  for (const [entryPath, content] of Object.entries(cssFiles)) {
    try {
      const result = await esbuild.transform(content, {
        loader: 'css',
        minify,
        target: ['chrome120'],
      })

      const outputPath = entryPath.replace(/^\//, '')
      outputFiles[outputPath] = result.code

      if (result.warnings.length > 0) {
        for (const warn of result.warnings) {
          warnings.push({ file: entryPath, message: warn.text })
        }
      }
    } catch (err) {
      // CSS transform failure — pass through original
      warnings.push({ file: entryPath, message: `CSS transform failed: ${err.message}` })
      outputFiles[entryPath.replace(/^\//, '')] = content
    }
  }

  // Normalize passthrough file paths (strip leading /)
  for (const [path, content] of Object.entries(passthroughFiles)) {
    const normalPath = path.replace(/^\//, '')
    if (normalPath !== path) {
      outputFiles[normalPath] = content
      delete outputFiles[path]
    }
  }

  const hasErrors = errors.length > 0
  console.log(`[esbuild] buildExtension done: success=${!hasErrors}, errors=${errors.length}, outputFiles=${Object.keys(outputFiles).length}`)
  if (errors.length > 0) console.error('[esbuild] errors:', errors)
  return {
    success: !hasErrors,
    files: outputFiles,
    resolvedPackages,
    rejectedPackages: rejected,
    errors,
    warnings,
    fallback: hasErrors
  }
}
